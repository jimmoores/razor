def gen_x64_cif_stub(f, symbol, inputs, outputs):
	"""Generate CIF stub for x64 architecture.
	Uses standalone assembly functions (ccsp_cif_x64_kcall_resched and
	ccsp_cif_x64_kcall) instead of inline asm, because the inline asm
	approach cannot safely handle the r14/r15 registers that the occam
	runtime uses for Wptr/sched but GCC may reassign freely.
	"""
	resched = (symbol["name"][0] == 'Y')
	in_regs = min(len(inputs), 1)
	out_regs = min(len(outputs), 1)
	offset = symbol["offset"]
	calltable_offset = 48 + offset * 8

	if len(inputs) > 1:
		for (n, i) in enumerate(inputs):
			if n >= 1:
				f.line("__sched->cparam[%d] = (word) (%s);" % ((n - 1), i))

	param0 = inputs[0] if in_regs > 0 else "0"

	if resched:
		f.line("{")
		f.indent()
		f.line("extern word ccsp_cif_x64_kcall_resched(void *, void *, word, void *);")
		f.line("void *__func = *(void **)((char *)__sched + %d);" % calltable_offset)
		if out_regs > 0:
			f.line("%s = (__typeof__(%s)) ccsp_cif_x64_kcall_resched((void *)__wptr, (void *)__sched, (word)(%s), __func);" % (
				outputs[0], outputs[0], param0))
		else:
			f.line("ccsp_cif_x64_kcall_resched((void *)__wptr, (void *)__sched, (word)(%s), __func);" % param0)
		f.line("__sched = (ccsp_sched_t *)((__wptr)[SchedPtr]);")
		if len(outputs) > 1:
			for (n, i) in enumerate(outputs):
				if n >= 1:
					f.line("*((word *)(&(%s))) = __sched->cparam[%d];" % (i, (n - 1)))
		f.outdent()
		f.line("}")
	else:
		f.line("{")
		f.indent()
		f.line("extern word ccsp_cif_x64_kcall(void *, void *, word, void *);")
		f.line("void *__func = *(void **)((char *)__sched + %d);" % calltable_offset)
		if out_regs > 0:
			f.line("%s = (__typeof__(%s)) ccsp_cif_x64_kcall((void *)__wptr, (void *)__sched, (word)(%s), __func);" % (
				outputs[0], outputs[0], param0))
		else:
			f.line("ccsp_cif_x64_kcall((void *)__wptr, (void *)__sched, (word)(%s), __func);" % param0)
		if len(outputs) > 1:
			for (n, i) in enumerate(outputs):
				if n >= 1:
					f.line("*((word *)(&(%s))) = __sched->cparam[%d];" % (i, (n - 1)))
		f.outdent()
		f.line("}")

def gen_x64_header(f):
	"""Generate x64 CIF header macros."""
	# sizeof(word) for x64 = 8
	sizeof_word = 8

	# ccsp_cif_external_call
	f.begin_macro()

	f.begin_line()
	f.line("#define ccsp_cif_external_call(func, stack, result)")

	f.indent()
	f.line("do {")

	f.indent()
	f.line("word __tmp_sp;")
	f.line("__asm__ __volatile__ (\"\\n\"")
	f.indent()

	f.begin_asm()
	f.line("\tmovq %%rsp, %0")		# save sp
	f.line("\tmovq %3, %%rsp")		# switch to provided stack
	f.line("\tcallq *%2")			# call function (func)
	f.line("\tmovq %0, %%rsp")		# restore sp
	f.end_asm()
	f.line(": \"=&r\" (__tmp_sp), \"=a\" (result)")
	f.line(": \"r\" (func), \"r\" (stack)")
	f.line(": \"cc\", \"memory\", \"rcx\", \"rdx\", \"rsi\", \"rdi\", \"r8\", \"r9\", \"r10\", \"r11\"")

	f.outdent()
	f.line(");")
	f.outdent()

	f.begin_line()
	f.add ("} while (0)")
	f.end_line(end_macro = True)
	f.outdent()

	# ccsp_cif_jump
	f.begin_macro()

	f.begin_line()
	f.line("#define ccsp_cif_jump(wptr, addr)")

	f.indent()
	f.line("do {")

	f.indent()
	f.line("__asm__ __volatile__ (\"\\n\"")
	f.indent()

	f.begin_asm()
	f.line("\tmovq %0, %%r14")		# r14 = Wptr
	f.line("\tjmpq *%1")			# jump to addr
	f.end_asm()
	f.line(": /* no outputs */")
	f.line(": \"r\" (wptr), \"q\" (addr)")
	f.line(": \"memory\"")

	f.outdent()
	f.line(");")
	f.outdent()

	f.begin_line()
	f.add ("} while (0)")
	f.end_line(end_macro = True)
	f.outdent()

	# ccsp_cif_occam_call
	f.begin_macro()

	f.begin_line()
	f.line("#define ccsp_cif_occam_call(sched, stack, ws, func, top)")

	f.indent()
	f.line("do {")

	f.indent()
	f.line("register word __r_ws __asm__(\"rdi\") = (word)(ws);")
	f.line("register word __r_sched __asm__(\"rsi\") = (word)(sched);")
	f.line("register word __r_func __asm__(\"rax\") = (word)(func);")
	f.line("register word __r_topoff __asm__(\"r8\") = (word)(top * %d);" % sizeof_word)
	f.line("(void)(stack);")
	f.line("__asm__ __volatile__ (\"\\n\"")
	f.indent()

	f.begin_asm()
	# Save SP at ws[top], setup workspace
	# r8 = top offset (from register variable)
	f.line("\tmovq %%rdi, %%r14")			# r14 = ws (Wptr)
	f.line("\taddq %%r14, %%r8")			# r8 = ws + top_offset
	f.line("\tmovq %%rsp, (%%r8)")			# ws[top] = SP
	f.line("\tmovq %%rbp, 8(%%r8)")			# ws[top+1] = rbp
	# Also save top_offset+ws pointer at ws[top+2] for restoration
	f.line("\tmovq %%r8, 16(%%r8)")			# ws[top+2] = save_area_ptr
	# Store return label at ws[0] (IptrSucc) and ws[-1] (Iptr)
	f.line("\tleaq 0f(%%rip), %%r8")
	f.line("\tmovq %%r8, (%%r14)")			# ws[0] = return label
	f.line("\tmovq %%r8, -8(%%r14)")		# ws[-1] = return label
	# Switch to private workspace stack
	f.line("\tleaq -256(%%r14), %%rsp")
	f.line("\tandq $-16, %%rsp")
	# Set scheduler pointer for occam code (r15 = sched)
	f.line("\tmovq %%rsi, %%r15")			# r15 = sched
	f.line("\tjmpq *%%rax")				# jump to func
	f.line("0:")
	# Return from occam: r14 = ws + 32 due to I_RET frame pop. Undo that.
	f.line("\tsubq $32, %%r14")			# undo I_RET frame pop
	# Reload topoff from compile-time constant instead of clobbered r8.
	# r8 was used by the occam function (caller-saved) and has garbage.
	f.line("\tmovq %4, %%r8")			# r8 = top_offset (constant)
	f.line("\taddq %%r14, %%r8")			# r8 = ws + top_offset
	f.line("\tmovq (%%r8), %%rsp")			# restore SP from ws[top]
	f.line("\tmovq 8(%%r8), %%rbp")			# restore rbp from ws[top+1]
	f.end_asm()
	f.line(": \"+r\" (__r_ws), \"+r\" (__r_sched), \"+r\" (__r_func), \"+r\" (__r_topoff)")
	f.line(": \"i\" ((word)(top * %d))" % sizeof_word)  # %4 = topoff constant
	# Clobber ALL registers the occam function may modify
	f.line(": \"cc\", \"memory\",")
	f.line("  \"rcx\", \"rdx\", \"r9\", \"r10\", \"r11\",")
	f.line("  \"rbx\", \"r12\", \"r13\", \"r14\", \"r15\"")

	f.outdent()
	f.line(");")
	f.outdent()

	f.begin_line()
	f.add ("} while (0)")
	f.end_line(end_macro = True)
	f.outdent()

