def gen_aarch64_header(f):
	# ccsp_cif_external_call
	f.begin_macro()

	f.begin_line()
	f.line("#define ccsp_cif_external_call(func, stack, result)")

	f.indent()
	f.line("do {")
	
	f.indent()
	f.line("word __tmp_sp;")
	f.line("__asm__ __volatile__ (\"\\n\"")
	f.indent()

	f.begin_asm()
	f.line("\tmov %0, sp")
	f.line("\tmov sp, %3")
	f.line("\tblr %2")
	f.line("\tmov sp, %0")
	f.end_asm()
	f.line(": \"=&r\" (__tmp_sp), \"=r\" (result)")
	f.line(": \"r\" (func), \"r\" (stack)")
	f.line(": \"cc\", \"memory\", \"x0\", \"x1\", \"x2\", \"x3\", \"x4\", \"x5\", \"x6\", \"x7\", \"x8\", \"x9\", \"x10\", \"x11\", \"x12\", \"x13\", \"x14\", \"x15\", \"x16\", \"x17\", \"x30\"")

	f.outdent()
	f.line(");")
	f.outdent()
	
	f.begin_line()
	f.add ("} while (0)")
	f.end_line(end_macro = True)
	f.outdent()

	# ccsp_cif_jump
	f.begin_macro()

	f.begin_line()
	f.line("#define ccsp_cif_jump(wptr, addr)")

	f.indent()
	f.line("do {")
	
	f.indent()
	f.line("__asm__ __volatile__ (\"\\n\"")
	f.indent()

	f.begin_asm()
	f.line("\tmov x28, %0")
	f.line("\tbr %1")
	f.end_asm()
	f.line(": /* no outputs */")
	f.line(": \"r\" (wptr), \"r\" (addr)")
	f.line(": \"memory\"")

	f.outdent()
	f.line(");")
	f.outdent()
	
	f.begin_line()
	f.add ("} while (0)")
	f.end_line(end_macro = True)
	f.outdent()
	
	# ccsp_cif_occam_call
	# Use register variables for deterministic register assignment.
	# The occam function destroys ALL callee-saved registers (x19-x28),
	# so they must be in the clobber list.  With generic "=r" constraints,
	# GCC might assign operands to those same registers, causing corruption.
	# Fixed-register variables avoid this entirely.
	f.begin_macro()

	f.begin_line()
	f.line("#define ccsp_cif_occam_call(sched, stack, ws, func, top)")

	f.indent()
	f.line("do {")

	f.indent()
	f.line("register word __r_ws __asm__(\"x0\") = (word)(ws);")
	f.line("register word __r_sched __asm__(\"x1\") = (word)(sched);")
	f.line("register word __r_func __asm__(\"x9\") = (word)(func);")
	f.line("(void)(stack);")
	f.line("__asm__ __volatile__ (\"\\n\"")
	f.indent()

	f.begin_asm()
	# Save SP, x29, x30 to workspace at ws[top..top+2]
	f.line("\tmov x28, x0")			# x28 = ws (Wptr)
	f.line("\tadd x3, x28, %3")		# x3 = ws + top*sizeof(word)
	f.line("\tmov x10, sp")
	f.line("\tstr x10, [x3]")		# ws[top] = SP
	f.line("\tstr x29, [x3, #8]")		# ws[top+1] = x29
	f.line("\tstr x30, [x3, #16]")		# ws[top+2] = x30
	# Store return label at ws[0] (Iptr) and ws[-1] (backup)
	f.line("\tadr x3, 0f")
	f.line("\tstr x3, [x28]")		# ws[0] = return label
	f.line("\tstur x3, [x28, #-8]")		# ws[-1] = return label
	# Switch to private workspace stack before jumping to occam.
	# This prevents the occam function's C calls from using sched->stack
	# where other processes would overwrite saved state.
	f.line("\tsub x3, x28, #256")
	f.line("\tand x3, x3, #-16")
	f.line("\tmov sp, x3")
	# Set scheduler pointer for occam code (x25 = sched)
	f.line("\tmov x25, x1")			# x25 = sched
	f.line("\tbr x9")			# jump to func
	f.line("0:")
	# Return from occam: x28 = ws + 32 due to I_RET frame pop.
	# compose_aarch64_return always pops 32 bytes (4 words) for the
	# I_CALL frame that OccamCall doesn't push. Undo that shift.
	f.line("\tsub x28, x28, #32")		# undo I_RET frame pop
	f.line("\tadd x3, x28, %3")		# x3 = ws + top*sizeof(word)
	f.line("\tldr x10, [x3]")		# x10 = saved SP
	f.line("\tmov sp, x10")			# restore SP
	f.line("\tldr x29, [x3, #8]")		# restore x29
	f.line("\tldr x30, [x3, #16]")		# restore x30
	f.end_asm()
	f.line(": \"+r\" (__r_ws), \"+r\" (__r_sched), \"+r\" (__r_func)")
	f.line(": \"i\" (top * sizeof(word))")
	# Clobber ALL registers the occam function may modify.
	# Operands use x0, x1, x9 (+r).  We save/restore x29, x30.
	# Everything else is fair game for the occam function.
	f.line(": \"cc\", \"memory\",")
	f.line("  \"x2\", \"x3\", \"x4\", \"x5\", \"x6\", \"x7\", \"x8\", \"x10\",")
	f.line("  \"x11\", \"x12\", \"x13\", \"x14\", \"x15\", \"x16\", \"x17\",")
	f.line("  \"x19\", \"x20\", \"x21\", \"x22\", \"x23\", \"x24\", \"x25\",")
	f.line("  \"x26\", \"x27\", \"x28\"")

	f.outdent()
	f.line(");")
	f.outdent()

	f.begin_line()
	f.add ("} while (0)")
	f.end_line(end_macro = True)
	f.outdent()

def output_cif(defines, symbol_list, symbols, fn):
	if "TARGET_CPU_386" in defines:
		arch_header = gen_i386_header
		arch_generator = gen_i386_cif_stub
	elif "TARGET_CPU_MIPS" in defines:
		warn("generating CIF stubs for unsupported architecture...")
		arch_header = gen_mips_header
		arch_generator = gen_mips_cif_stub
	elif "TARGET_CPU_PPC64" in defines:
		warn("generating CIF stubs for unsupported architecture...")
		arch_header = gen_ppc64_header
		arch_generator = gen_ppc64_cif_stub
	elif "TARGET_CPU_SPARC" in defines:
		warn("generating CIF stubs for unsupported architecture...")
		arch_header = gen_sparc_header
		arch_generator = gen_sparc_cif_stub
	elif "TARGET_CPU_AARCH64" in defines:
		warn("generating CIF stubs for unsupported architecture...")
		arch_header = gen_aarch64_header
		arch_generator = gen_aarch64_cif_stub
	elif "TARGET_CPU_X64" in defines:
		arch_header = gen_x64_header
		arch_generator = gen_x64_cif_stub
	else:
		die("unable to generate CIF stubs for unknown architecture.")

	f = HeaderWriter(open(fn, "w"))
	output_header(f, fn, False)

	arch_header(f)

	for name in symbol_list:
		symbol = symbols[name]
		if not symbol.get("unsupported"):
			gen_cif_stub(f, symbol, arch_generator)

	output_footer(f, fn)
	f.close()

def main(args):
	if len(args) < 3:
		die("usage: <type> <output.h> <input1> [<input2> ...]")
	
	type = args[0]
	output = args[1]
	inputs = args[2:]
	
	defines = {}
	calls = {}
	symbols = {}
	for input in inputs:
		load_defines(defines, input)
		load_symbols(calls, symbols, input)
	
	test_symbol_requirements(defines, calls, symbols)
	enumerate_symbols(symbols)

	symbol_list = safe_sorted(
		symbols.keys(),
		key=lambda x: symbols[x]["offset"]
	)

	if type == "--kitable":
		output_kitable(symbol_list, symbols, output)
	elif type == "--calltable":
		output_calltable(symbol_list, symbols, output)
	elif type == "--cif":
		output_cif(defines, symbol_list, symbols, output)
	else:
		die("type must be: --kitable, --calltable or --cif")

if __name__ == "__main__":
	main(sys.argv[1:])

